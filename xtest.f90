DOUBLE PRECISION FUNCTION DIGAMD(X)
!C***********************************************************************
!C*                                                                     *
!C*  FORTRAN CODE WRITTEN FOR INCLUSION IN IBM RESEARCH REPORT RC20525, *
!C*  'FORTRAN ROUTINES FOR USE WITH THE METHOD OF L-MOMENTS, VERSION 3' *
